    <p>Bongkemaliq  merupakan salah satu situs sejarah dan cagar budaya yang mengandung nilai nilai agama dan budaya. Bongkemaliq ini berada di Lombok tepatnya di Desa kerongkong, Kecamatan suralaga, Kabupaten Lombok Timur. Bongkemaliq ini merupakan bong yang di keramatkan oleh suku Sasak , khususnya masyarakat desa kerongkong dan sekitarnya, sehingga nama bongkemaliq ini diabadikan menjadi salah satu dusun yang terdapat didesa kerongkong yaitu dusun bongkemaliq.  </p><p>Bagi masyarakat Sasak, kemaliq memiliki makna yang sangat sakral bukan hanya bagi diri pribadi tetapi juga kehidupan sosial. Bongkemaliq sendiri terdiri dari dua kata yaitu : bong artinya gentong air, kemaliq artinya keramat. Dari sana kita bisa mengambil kesimpulan bahawa bongkemaliq adalah suatu tempat yang dikeramatkan dan dipercaya bisa menyembuhkan peyakit tentunya dengan ridha allah.</p><p>Mengenai tahun berdirinya tidak bisa dipstikan, hal ini dikarnakan tidak ada bukti otentik yang bisa dijadikan refrensi, namun beberapa cerita yang berkembang dimasyarakat secara turun temurun, bongkemaliq ini didirikan pada hari senin tanggal 12 rabiul awal (maulid nabi) dan tidak jauh dari berdirinya kesultanan yogyakarta sekitar tahun 1750 an. Bongkemaliq ini sendiri didirikan oleh bebrapa tokoh sufi yang membangun desa kerongkong diantaranya : jero artika laki, jero artika bini, jero hamsiyah, dan dibantu oleh pengikutnya dikenal dengan nama balok bangkol.</p><p>Masyarakat Sasak pada era tersebut merupakan masyarakat sufistik. Mereka sangat percaya segala sesuatu yang terdapat di bumi merupakan pemberian Allah SWT, yang harus dijaga sebaik-baiknya dan digunakan untuk kemaslahatan manusia. Dengan ketulusan dan kerendahan hati mereka, sehinga bongkemaliq yang didirikan tersebut diberikan keberkatan, sehingga tempat tersebut dijadikan sebagai tempat memohon ridho allah berupa kesehatan dan  terhindar dari hal buruk dengan harapan agar kerukunan hidup tetap terjaga. Keberadaan </p><p>bongkemaliq tidak berdiri sendiri, namun terdiri dari beberapa bagian yang menjadi satu kesatuan, diantaranya :
    <ul>1.  Bong (Gentong Air)<br>
